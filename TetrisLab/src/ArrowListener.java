/**
 * The ArrowListener Interface has functions for different key presses
 * (up, down, left, right arrows and space).
 * @author Block Lab Author(s)
 * @author Nelson Gou
 * @version 3/4/22
 */
public interface ArrowListener
{
    void upPressed();
    void downPressed();
    void leftPressed();
    void rightPressed();
    void spacePressed();
}